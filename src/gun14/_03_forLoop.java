package gun14;

public class _03_forLoop {
    public static void main(String[] args) {
        // 0 dan 10(dahil) kadar sayıları ekrana yazdırın
        // 10 dan 0(dahil) kadar ekrana yazdırın(ayrı for)

        for (int i=0;i<=10;i++)
            System.out.println("i:"+i);
        for (int i=10;i>=0;i--)
            System.out.println("i:"+i);

    }
}
