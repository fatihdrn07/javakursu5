package _odev2_;

public class _01_soru10 {
    public static void main(String[] args) {
        //# do while loop kullanınız.
        //> 0 ile 30 arasındaki bütün çift sayıları yazdırınız.
        int sayi=0;
        do {
            sayi+=2;
            System.out.println(sayi);
        }while (sayi<28);
    }
}
